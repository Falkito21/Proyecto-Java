    //BOTON GUARDAR CLIENTE ****************************************************
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //VARIABLE cliente DE TIPO CLIENTE
        Cliente cliente = new Cliente();
        //FUNCION QUE SETEA - INICIA EL VALOR 
        //A CLIENTE LE SETEAMOS EL VALOR DEL NOMBRE, 
        //CON EL VALOR QUE ME TRAE(getText) DE txtNombre
        cliente.setNombre(this.txtNombre.getText());
        cliente.setApellido(this.txtApellido.getText());
        cliente.setEmail(this.txtEmail.getText());
        cliente.setTelefono(this.txtTelefono.getText());
        
        //SI LA VARIABLE lblId NO ESTA VACIO LE SETEA EL VALOR QUE CORRESPONDE
        if (!StringUtils.isEmptyOrWhitespaceOnly(lblId.getText())){
            cliente.setId(lblId.getText());
        }
        
        //LLAMAMOS A ClienteDao 
        ClienteDao dao = new ClienteDao();
        
        //LLAMAMOS A LA FUNCION GUARDAR PARA SE DE CUENTA AUTOMATICAMENTE -
        // - SI TIENE QUE GUARDAR UN CLIENTE O ACTUALIZARLO
        dao.guardar(cliente);
        
        
        actualizarLista();
        JOptionPane.showMessageDialog(rootPane,"El cliente se guardó correctamente");
        limpiarCajasDeTexto();
    }//GEN-LAST:event_btnGuardarActionPerformed
    
    //BOTON ELIMINAR CLIENTE ***************************************************
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //LO PRIMERO ES SABER QUE ELEMENTO ESTA SELECCIONADO
        //getSelectedIndex(): ME DEVUELVE EL VALOR DE LA POSION DONDE SE -
        //- ENCUENTRA EL ELEMENTO SELECCIONADO
        int indice = this.listClientes.getSelectedIndex();
        Cliente cliente = lista.get(indice);
        ClienteDao dao = new ClienteDao();
        dao.eliminar(cliente.getId());
        //ELIMINAMOS-REMOVEMOS EL ELEMENTO DE LA POSICION 'indice'
        //lista.remove(indice);
        actualizarLista();
        JOptionPane.showMessageDialog(rootPane,"Se elimino correctamente.");
    }//GEN-LAST:event_btnEliminarActionPerformed
    
