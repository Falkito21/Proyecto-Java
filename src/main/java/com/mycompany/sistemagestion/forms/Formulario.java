    //CREO UNA FUNCION QUE ACTUALIZA LA LISTA DEL FORMULARIO *******************
    private void actualizarLista(){
        ClienteDao dao = new ClienteDao();
        lista = dao.listar();
        //MODEL:MODELO DE DATOS
        DefaultListModel datos = new DefaultListModel();
        //LISTA.SIZE = INDICA EL TAMAÑO DE LA LISTA
        for (int i = 0; i < lista.size(); i++){
            //RECORRE LA LISTA Y GUARDA EL ELEMENTO EN LA VARIABLE cliente DE tipo Cliente
            Cliente cliente = lista.get(i);
            //AGREGA A LA LISTA QUE MOSTRAMOS LOS ELEMENTOS GUARDADOS EN LA VARIABLE cliente
            //MOSTRANDO EL ELEMENTO NombreCompleto DE LA VARIABLE cliente
            datos.addElement(cliente.getNombreCompleto()); 
        }
        this.listClientes.setModel(datos);
    }
    
